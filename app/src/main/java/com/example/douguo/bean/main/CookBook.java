public class CookBook extends BaseModel {
    private int id;
    private String i;
    private int w;
    private int h;
    private String u;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getI() {
        return i;
    }

    public void setI(String i) {
        this.i = i;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public String getU() {
        return u;
    }

    public void setU(String u) {
        this.u = u;
    }
}
